function activeLinkStyle({
  isActive,
  isPending,
}: {
  isActive: boolean;
  isPending: boolean;
}) {
  return {
    fontWeight: isActive ? '500' : undefined,
    color: isPending ? 'grey' : isActive ? 'hsl(var(--primary))' : 'hsl(var(--foreground))',
  };
}
